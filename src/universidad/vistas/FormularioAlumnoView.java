    private void jButton1BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1BuscarActionPerformed

try {
            Integer dni = Integer.parseInt(jTextField1Dni.getText());
            alumnoActual= alumnoData.buscarAlumnoPorDni(dni);
            
            if (alumnoActual!=null) {
        jTextField3Nombre.setText(alumnoActual.getNombre());
        jTextField2Apellido.setText(alumnoActual.getApellido());
        jRadioButton1Estado.setSelected(alumnoActual.isActivo());
        LocalDate lc= alumnoActual.getFechanacimiento();
        java.util.Date date= java.util.Date.from(lc.atStartOfDay(ZoneId.systemDefault()).toInstant());
        jDateChooser1Fecha.setDate(date);
      
                
                
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showConfirmDialog(null, "Debe ingresar un numero valido");
        }







        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1BuscarActionPerformed

