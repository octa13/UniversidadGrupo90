    private void jButton4GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4GuardarActionPerformed


        try{
    Integer dni = Integer.parseInt(jTextField1Dni.getText());
    String nombre=jTextField3Nombre.getText();
    String apellido=jTextField2Apellido.getText();

    if(nombre.isEmpty()|| apellido.isEmpty()){
      
        JOptionPane.showMessageDialog(this, "no puede haber campos vacios");
        return;
}
java.util.Date sfecha=jDateChooser1Fecha.getDate();
LocalDate fechanacimiento=sfecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
Boolean estado=jRadioButton1Estado.isSelected();

if(alumnoActual==null){
   
    alumnoActual=new Alumno(dni, apellido, nombre,fechanacimiento, estado);
    alumnoData.guardarAlumno(alumnoActual);

}else{

  alumnoActual.setDni(dni);
alumnoActual.setApellido(apellido);
alumnoActual.setNombre(nombre);
alumnoActual.setFechanacimiento(fechanacimiento);

alumnoData.modificarAlumno(alumnoActual);

}


}catch(NumberFormatException nfe){

   JOptionPane.showMessageDialog(this, "Debe ingresar un Dni valido");

}






        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4GuardarActionPerformed

