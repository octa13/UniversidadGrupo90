    private void jButton4GuardarGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4GuardarGuardarActionPerformed


        try{
    Integer idMateria = Integer.parseInt(jTextField1Codigo.getText());
    String nombre= jTextField2Nombre.getText();
    Integer anioMateria=Integer.parseInt(jTextField3Anio.getText());

    if(nombre.isEmpty()|| anioMateria==null){
      
        JOptionPane.showMessageDialog(this, "no puede haber campos vacios");
        return;
}

Boolean estado=jRadioButton1Estado.isSelected();

if(materiaActual==null){
   
    materiaActual=new Materia(idMateria, nombre,anioMateria, estado);
    materiaData.guardarMateria(materiaActual);

}else{

materiaActual.setIdMateria(idMateria);

materiaActual.setNombre(nombre);

materiaData.modificarMateria(materiaActual);

}


}catch(NumberFormatException nfe){

   JOptionPane.showMessageDialog(this, "Debe ingresar un Dni valido");

}






        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4GuardarGuardarActionPerformed

